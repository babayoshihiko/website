<a name="core">
<h2>Il Fink Core Team</h2>
</a>

<p>The Fink Core Team &egrave; responsabile di diversi aspetti della gestione centrale del progetto Fink.  I membri del Team lavorano   allo stesso programma fink  
e
coordinano i contributi di altri a questo programma; essi fanno da
gestori per tutti i  packages essenziali nella collezione Fink,
e assicurano che il bootstrapping con questi  packages essenziali funzioni;
essi gestiscono le releases del programma fink program e i programmi relativi;
essi gestiscono la creazione e release delle pricipali distribuzioni fink,
incluse le distribuzioni sorgente e binary.  Essi anche aiutano a coordinare gli altri gruppi di volontari che lavorano al progetto Fink. 
</p>

<p>Gli attuali  membri del fink core team sono Max Horn, Daniel Macks, Dave
Morrison, Peter O'Gorman, and Ben Reed.  Essi sono reperibili a &lt;fink-core@lists.sourceforge.net&gt;.
</p>

