<h1>Fink's logo</h1>

<div align="right">written October 25th, 2003</div>
<p>
The past three months of 2003 have been very interesting. <br /> 
We conducted a logo creation <a href=http://fink.sourceforge.net/logo.php> contest </a>.
The support from our community was almost overwhelming with participants from all over the 
world we learned how many are interested in Fink and willing to share their time and 
expertise to create something for Fink.<br />
More than <b>85</b> pieces of art have been submitted and we had to choose a winner. 
To make this as transparent as possible to you all, I shall explain how we did that.
</p>
<p>
The, at the time, three main Administrators:
Max Horn
Benjamin Reed
Dave R. Morrison 
and I had to decide on our four personal favourites in the first round. 
After collecting all the information, we decided on four overall favourites, those four 
remaining pieces of work were then voted on by distributing points.  
A sample vote could have looked like this:
<br />
<ol>
<li>Piece A ? 30 points</li>
<li>Piece B ? 25 points</li>
<li>Piece C ? 20 points</li>
<li>Piece D ? 15 points</li>
</ol>
<br />
After receiving all the votes from the main Administrators summing up the points and adding
my own one of our participants had to be the victor and I am proud to present his work as 
our new, official Fink logo.
<br />
<img src="../img/logobig.png" alt="Big logo" width="640" height="317"> </p> 
<p> This is the work of <b>Paul Enste</b>. For those of you who are macwelt.de 
  premium members a little interview can be found here</a>. Since I know that 
  not all of you might be able to access that nor able to comprehend German well 
  enough, there will be a follow up interview with Mister Enste in English as 
  soon as time permits. </p>
<p>
Mister Enste and I are still working on a licensing agreement, it will be posted as soon as
it has been formally agreed upon, until then <b>every third party</b> is forbidden to 
display or otherwise use this logo until further notice has been given here or elsewhere 
on the Fink homepage. 
</p>
<p>
We hope that you all enjoy the new look, but for all that we know that not everyone might 
take a fancy to it. Comments are of course welcome. <br />
You may voice your critique or praise on fink-users@lists.sourceforge.net 
</p>
