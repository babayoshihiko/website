Other people, including other Fink project members, may have different
views.
</blockquote>

<p align="right">written October 23rd, 2001</p>

<p>
Here's the story of the relations between the Fink project and
the ports archive at forked.net.
Once more, an unpleasant story, but I want to make this known to the
public.
</p>

<p>
The <a href="http://macosx.forked.net/">macosx.forked.net</a> site
offers ported Open Source software packaged in .pkg installer
packages.
The site has undergone several changes in the last two weeks and now
requires a subscription fee to access the ports.
For a short while the site also offered a CD; this seems to have been
widthdrawn now.
The person behind the website is Jon Daniels, who seems reluctant to
use his real name and prefers the nickname "apex".
(I only found out this last name through the whois domain registration
database.)
</p>
<p>
A significant part of the packages offered by forked.net are straight
repacks of Fink packages, with the package description and usage info
removed.
The packages themselves have no indication of their real origin,
instead the splash screen states "brought to you by
macosx.forked.net".
Some of these packages have a small "ported by fink" note in the
package listings on the website, but there is quite a number that is
unmarked.
In addition, macosx.forked.net doesn't have any information towards
the licenses or the source code, thereby violating the GPL and
possibly other licenses.
</p>
<p>
I recently contacted Jon about the situation since I felt he was
unfairly taking credit for other people's work and taking advantage of
that.
His initial reaction was quite positive, and he asked me what exactly
I wanted.
I told him, but unfortunately he didn't follow through.
Instead, he took a range of steps that make it obvious he's desperate
to make money from the site, bespite his original claims.
</p>
<p>
Our <a href="forked-thread.php">e-mail exchange</a> is available for
reading.
Unfortunately, the initial exchange took place on the forked.net
bulletin board, and Jon deleted the topic.
As usual, judge for yourself.
</p>
<p>
-Christoph Pfisterer
</p>

<p align="right">Update October 25th, 2001</p>
<p>
One day after I posted this page, the macosx.forked.net front page
changed again.
All PayPal buttons are gone, the downloads are freely accessible
again, and there are news items mentioning Fink and the GPL.
I guess I should have made daily backup copies of their front page.
It will be interesting to see how long it lasts this time...
</p>


