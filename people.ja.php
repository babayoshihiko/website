<h1>貢献している人々</h1>

<p>
Fink プロジェクトはボランティアによって成り立っています。
それぞれの Fink パッケージは、メンテナが力を尽くして動くようにしているのです。
また、 Fink のプログラム自体を作っている人たちもいます。
ウェブサイトもこうして最新に維持されています。
サポートは、メーリングリストや IRC チャンネルで行っています。
こういった作業はどれも大変な作業で、多くの時間を割いている人々に「ありがとう」を言いたいと思います。
以下は Fink に貢献してくれた人々のほんの一部です。
</p>

<p>
非常に多くの人々が Fink を助けてくれたため、全てをあげるのは困難です。
もし自分がここに掲載されるに値するのであれば、メールを送って下さい。
掲載の検討をいたします。
</p>

<p>
斜体字は IRC チャンネルでのニックネームです。
</p>

