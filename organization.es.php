<h1>Organización de Fink</h1>

<p>El proyecto Fink funciona a través de voluntarios, y una estructura de 
organización flexible. Al presente, el Fink Core Team y los Traductores de
Fink son los grupos más organizados dentro del proyecto Fink.</p>

<a name="core"><h2>El Fink Core Team</h2></a>

<p>El Fink Core Team es responsable de varios aspectos del manejo central 
del proyecto de Fink.  Los miembros del grupo trabajan en el programa fink 
y coordinan las contribuciones de otros al programa; funjen como 
Maintainers de los paquetes "esenciales" en la colección de Fink,
y se asegura de que el bootstrapping funcione con estos paquetes 
esenciales; ellos manejan las liberaciones del programa fink y los 
programas relacionados; manejan las creaciones y liberación de las 
distribuciones mayores de fink, incluyendo la fuente y la binaria.
También ayudan a coordinar a los otros grupos de voluntarios que trabajan 
en el proyecto de Fink.</p>

<p>Los miembros actuales del fink core son Max Horn, Daniel Macks, Dave
Morrison, Peter O'Gorman, and Ben Reed.  Ellos pueden ser localizados en 
&lt;fink-core@lists.sourceforge.net&gt;.
</p>

