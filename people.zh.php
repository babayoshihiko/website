<h1>贡献者</h1>

<p>Fink 项目由一群志愿者所维护。每个软件包都有一个维护者来努力使它能够按我们希望的方式正常工作。另外，还有一些人投身于 fink 程序本身的开发。
网站需要保持更新，用户需要通过我们的邮件列表和我们的 IRC 频道进行支持。这些及很多另外的事情需要很多的工作，因此我们应该向那些牺牲自己的工余时间给社区的人说声“谢谢你！”。因此，我们用这个页面记录那些曾经以各种方式向 Fink 项目做出贡献的人的名单。
</p>

<p>在 Fink 走到今天的过程中，有太多的人提供过帮助，所以我们很难把你们的全部都列在这里。如果你认为你应该被列在这里但实际却没有，请给我们来信，我们会考虑在下一个版本添加进去。</p>

<p>斜体（括号内）的内容指在 IRC 频道中使用的昵称。</p>

