    <h2><a name="sources">3.1 Akzeptierte Quellen für Sicherheitslücken</a></h2>
      
      <p>Wer eine Sicherheitslücke für ein Fink-Paket meldet, muss auch
        sicher stellen, dass die Sicherheitslücke auch auf Mac OS X
