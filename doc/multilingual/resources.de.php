    <h2><a name="editors">4.2 Editoren</a></h2>
      
      <p>
      Es gibt viele Editor-Programme für die Bearbeitung von Textdateien.
      Die kurze Liste enthält unsere Empfehlungen. Einige Editoren sind
      kompliziert zu benutzen oder nur für die Kommandozeile, also nicht
      für jedermann. Das Ziel ist, dass für jeden etwas dabei ist.
      </p>
      <ul>
        <li>
        <a href="http://www.codingmonkeys.de/subethaedit/">SubEthaEdit</a> - Ein
        moderner grafoscher Editor für Mac OS X, der Rendezvous beherrscht.
        </li>
        <li>
        <a href="http://www.barebones.com/products/bbedit/index.shtml">BBEdit</a>
        - Eine vielseitiger und gut bekannter Editor, aber kostenplichtig.
        </li>
        <li>
        <a href="http://www.morphon.com/xmleditor/index.shtml">Morphon
        XML-Editor</a> - Ein kostenloser, spezialisierter XML-Editor.
        Sehr gut, um xml-Dateien zu editieren.
        </li>
        <li>
        <a href="http://free.abracode.com/cyclone/">Cyclone</a> - Ein
        kostenloses Interface für den Apple Text Encoding Converter.
        </li>
        <li>
        <a href="http://www.macalester.edu/~jaas/linebreak.html">LineBreak</a>
        - Ein einfaches Hilfsprogramm für Mac OS X, das Zeilenende in
        Textdokumenten konvertiert.
        </li>
      </ul>
    
