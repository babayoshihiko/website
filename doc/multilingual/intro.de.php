<h1>i18n - 1. Einführung</h1>
    
    
    <h2><a name="what">1.1 Was ist der Stand der Internationalisierung der Fink Webseiten?</a></h2>
      
      <p>
      Das Projekt Fink hat begonnen, seine Webseiten komplett zu
      internationalisieren, damit sie weltweit besser zugänglich ist.
      Besonderen Dank geht an Baba Yoshihiko, der einen Rahmen erstellt
      hat, in dem mehrere Sprachen verwendet werden können.
      </p>
    
    <h2><a name="languages">1.2 Sprachen</a></h2>
      
      <p>Für folgende Sprachen gibt es Freiwillige:</p>
      <ul>
        <li>Dänisch</li>
        <li>Holländisch</li>
        <li>Englisch (die Basisdokumentation)</li>
        <li>Französisch</li>
        <li>Deutsch</li>
        <li>Italienisch</li>
        <li>Japanisch</li>
        <li>Spanisch</li>
        <li>Vereinfachtes chinesisch</li>
      </ul>
    
    <h2><a name="organization">1.3 Organisation</a></h2>
      
      <p>
      Ein Diagramm des i18n-Team gibt es
      <a href="http://micmacfr.homeunix.org/web/organisation/">hier</a>.
      </p>
    
    <h2><a name="helping">1.4 Helfen</a></h2>
      
      <p>
      Wenn sie die Internationalisierungsbestrebungen unterstützen wollen,
      gibt es mehrere Möglichkeiten:
      </p>
      <ul>
        <li>
        Beteiligen sie sich an Diskussionen auf der Mailing-Liste <a href="/lists/fink-i18n.php">fink-i18n</a>. Hier werden
        Mechanismen der Internationalisierung diskutiert, neue Versionen
        von Dokumenten angekündigt und vieles anderes mehr.
        </li>
        <li>
        Schauen sie die Übersetzung in ihrer eigenen Sprache durch.
        Vielleicht können sie den Textfluss verbessern, Fehler beheben und
        so weiter.
        </li>
        <li>
        Werden sie selbst ein Übersetzer. Wenn ihre Sprache noch nicht
        vorhanden ist (aber auch wenn sie es ist), und sie denken, dass sie
        gute Übersetzungen liefern können, können sie gerne als
        freiwilliger Übersetzer für ihre Sprache mitarbeiten.
        <p>
        Beachten sie aber bitte, dass im Fall, dass sie als erster mit
        einer Sprache anfangen, recht viele Dokumente zu übersetzen sind.
        Ist das aber erst einmal geschafft, fällt nicht mehr viel Arbeit
        für Änderungen und Ergänzungen an.
        </p>
        </li>
      </ul>
    
