<h1>i18n - 4 Autres ressources</h1>
    

    

    <h2><a name="resources">4.1 Liens importants</a></h2>
      

      <p>L'internationalisation n'est pas une chose simple à mettre en place. Les ressources indiquées ci-dessous peuvent servir de base à une source auxiliaire d'information. Nous vous conseillons de les lire soigneusement si vous voulez en connaître plus sur le sujet.</p>

      <ol>
        <li><a href="http://www.w3.org/TR/unicode-xml/">Unicode in XML and
        other Markup Languages</a></li>

        <li><a href="http://www.w3.org/International/tutorials/tutorial-char-enc.html">Tutorial:
        Character sets &amp; encodings</a></li>

        <li><a href="http://www.w3.org/International/">W3C
        Internationalization Activity</a></li>

        <li><a href="http://www.cl.cam.ac.uk/~mgk25/unicode.html">UTF-8 and
        Unicode FAQ for Unix/Linux</a></li>
      </ol>
    

    <h2><a name="editors">4.2 Éditeurs de textes</a></h2>
      

      <p>Il existe un certain d'éditeurs de texte que vous pouvez utiliser pour modifier les fichiers. Voici une liste de ceux que nous vous recommandons. Certains éditeurs de texte sont difficiles à utiliser ou sont des outils en ligne de commande, et ne conviennent donc pas à tout un chacun. Nous avons essayé de donner une liste d'éditeurs de texte pouvant convenir à tout le monde.</p>

      <ul>
        <li><a href="http://www.codingmonkeys.de/subethaedit/">SubEthaEdit</a> - éditeur de texte graphique pour Mac OS X utilisant la technologie Rendezvous.</li>

        <li><a href="http://www.barebones.com/products/bbedit/index.shtml">BBEdit</a>
        - éditeur de texte très complet et bien connu, quoique non gratuit.</li>

        <li><a href="http://www.morphon.com/xmleditor/index.shtml">Morphon
        XML-Editor</a> - éditeur de texte XML gratuit, très bien adapté pour la modification des fichiers XML.</li>

        <li><a href="http://free.abracode.com/cyclone/">Cyclone</a> - interface gratuite pour le convertisseur d'encodage de texte d'Apple.</li>

        <li><a href="http://www.macalester.edu/~jaas/linebreak.html">LineBreak</a>
        - utilitaire pour Mac OS X qui convertit les fins de ligne dans les fichiers texte.</li>
      </ul>
    

    <h2><a name="moretools">4.3 Outils utiles</a></h2>
      

      <p>L'interface en ligne de commande ne convient pas à tout le monde. Les outils suivants sont généralement des interfaces graphiques d'outils en ligne de commande. Vous pouvez vous en servir si vous le désirez.</p>

      <ul>
        <li><a href="http://www.wincvs.org/download.html">MacCvsX 3.3</a>
        - interface graphique pour CVS (recommandée uniquement aux chefs d'équipe)</li>
      </ul>
    
