<h1>Organização do Fink</h1>

<p>O projeto Fink trabalha através de voluntários e uma estrutura
organizacional relativamente livre. Atualmente, o Time Central do Fink e os
Tradutores do Fink são os grupos mais altamente organizados dentro do projeto
Fink.</p>

<a name="core"><h2>O Time Central do Fink</h2></a>

<p>O Time Central do Fink é responsável por vários aspectos de gerenciamento
central do projeto Fink. Os membros to time trabalham no programa fink em si e
coordena contribuições de outros; eles trabalham como mantenedores de todos os
pacotes "essenciais" na coleção do Fink e garantem que a instalação inicial
funcione com esses pacotes essenciais; eles gerenciam as versões do programa
fink e programas relacionados; eles gerenciam a criação e publicação das
principais distribuições do fink, incluindo tanto as de código fonte como as
binárias. Eles também ajudam a coordenar outros grupos de voluntários que
trabalham no projeto Fink.</p>

<p>Os membros centrais atuais são Max Horn, Daniel Macks, Dave Morrison, Peter
O'Gorman e Ben Reed. Eles podem ser contactados através da lista
&lt;fink-core@lists.sourceforge.net&gt;.</p>

